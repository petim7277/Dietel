package PersonalProgress;

public class AreaPerimeterOfRectangle {
    public static void main(String[] args) {
        double width = 4.5 ;
        double height = 7.9;
        double area = width * height;
        System.out.printf("The area of the rectangle is : %f",area);

        System.out.println("       ");


        double perimeter = 2   *  (width + height);
        System.out.printf("The perimeter of the rectangle is :%f",perimeter);

    }
}
